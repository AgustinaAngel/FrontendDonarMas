import React, { useState, useContext } from 'react';
import { BrowserRouter as Router, Route, Routes, Link, Navigate } from 'react-router-dom';
import NavBar from './components/Navbar';
import IniciarSesion from './components/IniciarSesion';
import CrearCampa単as from './components/CrearCampa単as';
import './App.css';
import user from './context/user.js';
import Home from './components/Home';


function App() {
  const [token, setToken] = useState('');
  const [usuario, setUsuario] = useState({});
  const [tipo, setTipo] = useState('');
  const [mostrarHome, setMostrarHome] = useState(true); // Variable de estado para controlar la visibilidad de Home


  const userSettings = {
    token,
    usuario,
    tipo,
    setToken,
    setUsuario,
    setTipo,
  };


  const ocultarHome = () => {
    setMostrarHome(false);
  };


  return (
    <user.Provider value={userSettings}>
      <Router>
        <NavBar ocultarHome={ocultarHome} />
        <div className="marginCostados">
          {mostrarHome && <Home />}
          <Routes>
            <Route path="/inciosesion" element={<IniciarSesion />} path2="/campa単a" element2={<CrearCampa単as />} />
          </Routes>
        </div>
      </Router>
    </user.Provider>
  );
}


export default App;